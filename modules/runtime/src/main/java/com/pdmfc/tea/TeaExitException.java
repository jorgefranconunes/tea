package com.pdmfc.tea;

import com.pdmfc.tea.TeaFlowControlException;






/**************************************************************************
 *
 * 
 *
 **************************************************************************/

public final class TeaExitException
    extends TeaFlowControlException {





    private Integer _value;




/**************************************************************************
 *
 * 
 *
 **************************************************************************/

    public TeaExitException(final Integer value) {

        _value = value;
    }






/**************************************************************************
 *
 * @return The integer value passed to the exit statement.
 *
 **************************************************************************/

    public Integer getExitValue() {

        return _value;
    }


}






