package com.pdmfc.tea;

import com.pdmfc.tea.TeaFlowControlException;






/**************************************************************************
 *
 * 
 *
 **************************************************************************/

public final class TeaReturnException
    extends TeaFlowControlException {





    private Object _value;




/**************************************************************************
 *
 * @param value The value being returned by the function.
 *
 **************************************************************************/

    public TeaReturnException(final Object value) {

        _value = value;
    }






/**************************************************************************
 *
 * @return The value passed to the return statement.
 *
 **************************************************************************/

    public Object getReturnValue() {

        return _value;
    }


}






